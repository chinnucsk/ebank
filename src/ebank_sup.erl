-module(ebank_sup).
-version(1.0).
-behaviour(supervisor).

%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/0, start_atm/2, stop_atm/1]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([init/1]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link() -> supervisor:start_link({local, ?SERVER}, ?MODULE, no_args).

start_atm(Name, Port) -> supervisor:start_child(?SERVER, child(atm_sup, Name, [Port])).

stop_atm(Name) ->
	supervisor:terminate_child(?MODULE, {atm_sup, Name}),
	supervisor:delete_child(?MODULE, {atm_sup, Name}).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init(no_args) ->
	{ok, {{rest_for_one, 5, 2000}, [child(backend, none)]}}.

%% ====================================================================
%% Internal functions
%% ====================================================================
child(Module, none) -> 
	{Module, {Module, start_link, []},
	 permanent, brutal_kill, worker,[Module]}.

child(Module, Name, Args) ->
	{{Module, Name}, {Module, start_link, [Name]++Args},
	 permanent, brutal_kill, worker, [Module]}.
