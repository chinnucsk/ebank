-module(atm_sup).
-version(1.0).
-behaviour(supervisor).

%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/2]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([init/1]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link(Name, Port) -> supervisor:start_link({local, ?SERVER}, ?MODULE, {Name, Port}).

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init({Name, Port}) ->
	{ok, {{rest_for_one, 5, 2000},
		  [child(atm, [Name]), child(webatm, [Name, Port])]}}.

%% ====================================================================
%% Internal functions
%% ====================================================================
child(Module, Args) ->
	{Module, {Module, start_link, Args},
	 permanent, brutal_kill, worker, [Module]}.

